public class Navio {
    public int posLinha;
    public int posColuna;
    public int orientacao;
    public int linha;
    public int coluna;

    public Navio(int posLinha, int posColuna, int linha, int coluna) {
        this.posLinha = posLinha;
        this.posColuna = posColuna;
        this.orientacao = orientacao;
        this.linha = linha;
        this.coluna = coluna;
    }
}